// 语言上下文
interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: Translations;
  isRTL: boolean;
  isChinese: boolean;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

// Provider组件
export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window !== 'undefined') {
      const saved = localStorage.getItem('app-language') as Language;
      if (saved && languages[saved]) {
        return saved;
      }
    }
    return detectBrowserLanguage();
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    if (typeof window !== 'undefined') {
      localStorage.setItem('app-language', lang);
      document.documentElement.lang = lang;
      document.documentElement.dir = languages[lang].rtl ? 'rtl' : 'ltr';
    }
  };

  useEffect(() => {
    if (typeof window !== 'undefined') {
      document.documentElement.lang = language;
      document.documentElement.dir = languages[language].rtl ? 'rtl' : 'ltr';
    }
  }, [language]);

  const value: LanguageContextType = {
    language,
    setLanguage,
    t: translationsData[language],
    isRTL: languages[language].rtl || false,
    isChinese: language === 'zh' || language === 'zh-TW',
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

// Hook
export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return context;
}